export function getRegionForCountry(code) {
  if (!code) return "";
  const normalized = String(code).trim().toUpperCase();
  if (EU_COUNTRIES.has(normalized)) return "EU";
  if (NA_COUNTRIES.has(normalized)) return "NA";
  if (KR_COUNTRIES.has(normalized)) return "KR";
  return "";
}

export function getPreferredServerLabel(codes = []) {
  const regions = (Array.isArray(codes) ? codes : [])
    .map((code) => getRegionForCountry(code))
    .filter(Boolean);
  if (regions.length < 2) return { label: "", note: "" };
  const counts = new Map();
  regions.forEach((region) => {
    counts.set(region, (counts.get(region) || 0) + 1);
  });
  const sorted = [...counts.entries()].sort((a, b) => b[1] - a[1]);
  if (sorted.length === 1) return { label: sorted[0][0], note: "" };
  if (sorted.length > 1 && sorted[0][1] > sorted[1][1]) {
    return { label: sorted[0][0], note: "majority region" };
  }
  const label = [...counts.keys()].join("/");
  return { label, note: "mixed regions" };
}
